        // It can be achieved with either abstract classes or interfaces in Java.

        // Abstract Class
        // An abstract class is a class that cannot be instantiated and is often used as a base class for other classes. It can contain both abstract and concrete methods.

        // Interface
        // An interface is a reference type in Java, it is similar to class, it is a collection of abstract methods. A class implements an interface, thereby inheriting the abstract methods of the interface.
